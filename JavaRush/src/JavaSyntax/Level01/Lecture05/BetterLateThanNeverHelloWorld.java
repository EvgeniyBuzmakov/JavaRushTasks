package JavaSyntax.Level01.Lecture05;
/*Лучше поздно, чем никогда: Hello world!
- Амиго, ты хочешь стать программистом?
- Ещё бы!
- Тогда быстрее пиши программу, которая выводит на экран фразу "Hello World!".
- Но… Это как-то банально!
- Именно. Амиго, твои мысли совпадают с моими, и это – отлично. Поэтому лучше выведи фразу, которую нам, роботам, стоит повторять в моменты неуверенности. Вот она: "Поцелуй мой блестящий металлический зад!"
*/
public class BetterLateThanNeverHelloWorld {
    public static void main(String[] args) {
        System.out.println("Поцелуй мой блестящий металлический зад!");
    }
}
